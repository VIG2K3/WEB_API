/**
 * Determine category based on destination name
 */
export const determineCategory = (destination: string): Category => {
  const lowerDest = destination.toLowerCase();
  
  for (const [category, keywords] of Object.entries(categoryKeywords)) {
    if (keywords.some(keyword => lowerDest.includes(keyword))) {
      return category as Category;
    }
  }
  
  return 'Attraction';
};

/**
 * Get a random image for a category
 */
export const getRandomImageForTrip = (category: Category): string => {
  const images = categoryImageBank[category];
  if (!images || images.length === 0) {
    return categoryImageBank.Attraction[0];
  }
  const randomIndex = Math.floor(Math.random() * images.length);
  return images[randomIndex];
};

/**
 * Get a consistent image based on trip ID with exact name matching first
 */
export const getConsistentImageForTrip = (tripId: string, category: Category, destination: string): string => {
  // FIRST: Try exact match by destination name
  if (exactImageMatches[destination]) {
    console.log(`✅ Exact match found for "${destination}"`);
    return exactImageMatches[destination];
  }
  
  // Also try case-insensitive partial match
  const lowerDest = destination.toLowerCase();
  for (const [name, image] of Object.entries(exactImageMatches)) {
    if (lowerDest.includes(name.toLowerCase()) || name.toLowerCase().includes(lowerDest)) {
      console.log(`🟡 Partial match: "${destination}" → "${name}"`);
      return image;
    }
  }
  
  // SECOND: Use hash-based consistent image from category
  const images = categoryImageBank[category];
  if (!images || images.length === 0) {
    return categoryImageBank.Attraction[0];
  }
  
  let hash = 0;
  for (let i = 0; i < tripId.length; i++) {
    hash = ((hash << 5) - hash) + tripId.charCodeAt(i);
    hash |= 0;
  }
  const index = Math.abs(hash) % images.length;
  console.log(`🔄 No exact match for "${destination}", using random ${category} image`);
  return images[index];
};

/**
 * Get image for a trip (main function to use)
 */
export const getImageForTrip = (trip: { _id?: string; destination: string; category?: Category }): string => {
  const category = trip.category || determineCategory(trip.destination);
  
  if (trip._id) {
    return getConsistentImageForTrip(trip._id, category, trip.destination);
  }
  return getRandomImageForTrip(category);
};

/**
 * Get all images for a category (for gallery views)
 */
export const getImagesByCategory = (category: Category): string[] => {
  return categoryImageBank[category] || [];
};

/**
 * Get a specific image by index for a category
 */
export const getImageByIndex = (category: Category, index: number): string => {
  const images = categoryImageBank[category];
  if (!images || index >= images.length) {
    return getRandomImageForTrip(category);
  }
  return images[index];
};

/**
 * Get total count of images in a category
 */
export const getImageCount = (category: Category): number => {
  return categoryImageBank[category]?.length || 0;
};

/**
 * Get all images across all categories
 */
export const getAllImages = (): Record<Category, string[]> => {
  return categoryImageBank;
};

/**
 * Search for images by keyword (useful for filtering)
 */
export const searchImages = (searchTerm: string): Array<{ category: Category; image: string }> => {
  const results: Array<{ category: Category; image: string }> = [];
  const term = searchTerm.toLowerCase();
  
  for (const [category, images] of Object.entries(categoryImageBank)) {
    if (category.toLowerCase().includes(term)) {
      results.push({ category: category as Category, image: images[0] });
    }
  }
  
  return results;
  
};

